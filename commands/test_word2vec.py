import re
import sys

class CheckSwearWord:
    PATTERNS = {
        'хуй': r'\b(пере|при|за|по|на|от|до|раз|вы|в|с|про|у|под|над)?ху[йё]?[а-яё]*|о?ху[ея]?[а-яё]*|\w*ху[яа]?[а-яё]*\b',
        'пизда': r'\b(пере|при|за|по|на|от|до|раз|вы|в|с|про|у|под|над)?пизд[аеио][а-яё]*|спизд[аеио]?\w*|пизде[цн][а-яё]*\b',
        'пидор': r'\b(пере|при|за|по|на|от|до|раз|вы|в|с|про|у|под|над)?пидор[аск]?[а-яё]*|п[ие]д[иа]р[аас]?[а-яё]*\b',
        'хер': r'\b(пере|при|за|по|на|от|до|раз|вы|в|с|про|у|под|над)?[о]?хер[аеи]?[а-яё]*|похер[ае]?\b',
        'хрен': r'\b(пере|при|за|по|на|от|до|раз|вы|в|с|про|у|под|над)?хр[её]н[а-яё]*|охрен[а-яё]*\b',
        'еб': r'\b(пере|при|за|по|на|от|до|раз|вы|в|с|про|у|под|над|отъ)?еб[а-яё]*\b',
        'bypass': r''.join((
            r'\w{0,5}[хx]([хx\s\!@#\$%\^&*+-\|\/]{0,6})',
            r'[уy]([уy\s\!@#\$%\^&*+-\|\/]{0,6})[ёiлeеюийя]\w{0,7}|\w{0,6}[пp]',
            r'([пp\s\!@#\$%\^&*+-\|\/]{0,6})[iие]([iие\s\!@#\$%\^&*+-\|\/]{0,6})',
            r'[3зс]([3зс\s\!@#\$%\^&*+-\|\/]{0,6})[дd]\w{0,10}|[сcs][уy]',
            r'([уy\!@#\$%\^&*+-\|\/]{0,6})[4чkк]\w{1,3}|\w{0,4}[bб]',
            r'([bб\s\!@#\$%\^&*+-\|\/]{0,6})[lл]([lл\s\!@#\$%\^&*+-\|\/]{0,6})',
            r'[yя]\w{0,10}|\w{0,8}[её][bб][лске@eыиаa][наи@йвл]\w{0,8}|\w{0,4}[еe]',
            r'([еe\s\!@#\$%\^&*+-\|\/]{0,6})[бb]([бb\s\!@#\$%\^&*+-\|\/]{0,6})',
            r'[uу]([uу\s\!@#\$%\^&*+-\|\/]{0,6})[н4ч]\w{0,4}|\w{0,4}[еeё]',
            r'([еeё\s\!@#\$%\^&*+-\|\/]{0,6})[бb]([бb\s\!@#\$%\^&*+-\|\/]{0,6})',
            r'[нn]([нn\s\!@#\$%\^&*+-\|\/]{0,6})[уy]\w{0,4}|\w{0,4}[еe]',
            r'([еe\s\!@#\$%\^&*+-\|\/]{0,6})[бb]([бb\s\!@#\$%\^&*+-\|\/]{0,6})',
            r'[оoаa@]([оoаa@\s\!@#\$%\^&*+-\|\/]{0,6})[тnнt]\w{0,4}|\w{0,10}[ё]',
            r'([ё\!@#\$%\^&*+-\|\/]{0,6})[б]\w{0,6}|\w{0,4}[pп]',
            r'([pп\s\!@#\$%\^&*+-\|\/]{0,6})[иeеi]([иeеi\s\!@#\$%\^&*+-\|\/]{0,6})',
            r'[дd]([дd\s\!@#\$%\^&*+-\|\/]{0,6})[oоаa@еeиi]',
            r'([oоаa@еeиi\s\!@#\$%\^&*+-\|\/]{0,6})[рr]\w{0,12}',
        )),
    }

    _compiled_patterns: dict[str, re.Pattern] | None = None

    @classmethod
    def _get_compiled_patterns(cls) -> dict[str, re.Pattern]:
        if cls._compiled_patterns is None:
            cls._compiled_patterns = {
                key: re.compile(pattern) for key, pattern in cls.PATTERNS.items()
            }
        return cls._compiled_patterns

    def check(self, word: str) -> bool:
        word_lower = word.lower()
        for regex in self._get_compiled_patterns().values():
            if regex.search(word_lower):
                return True
        return False

def main():
    sys.stdout.reconfigure(encoding='utf-8')
    words_dict = {
        'хуй': [
            'хуёк', 'хуишко', 'хуишечко', 'хуища', 'хуяра', 'хуёвина', 'хуёвый', 'хуевый',
            'охуенный', 'охуеть', 'хуярить', 'хуячить', 'дохуя', 'нахуй', 'нихуя',
            'похуй', 'охуенно'
        ],
        'пизда': [
            'пиздища', 'пиздец', 'пиздотня', 'пиздатый', 'пизданутый',
            'пиздить', 'спиздить', 'распиздеться', 'пиздануть',
            'напиздеть', 'напиздить'
        ],
        'пидор': [
            'пидарас', 'пидар', 'пидорок', 'пидорас', 'пидорский', 'пидорить',
            'пидорасина', 'пидорство'
        ],
        'хер': [
            'херик', 'херина', 'херище', 'охереть', 'херануть',
            'похер', 'охеренно', 'херовый', 'хер', 'отхерить', 'нахер'
        ],
        'хрен': [
            'хрен', 'хреновый', 'охренеть'
        ],
        'еб': [
            'ебать',
            'наебать',
            'отъебать',
        ]
    }

    checker = CheckSwearWord()

    for root, words in words_dict.items():
        print(f'\n{root}:')

        for word in words:
            match = checker.check(word)
            status = '✅' if match else '❌'
            print(f'  {status} {word}')


if __name__ == '__main__':
    r = main()
    sys.exit(r)