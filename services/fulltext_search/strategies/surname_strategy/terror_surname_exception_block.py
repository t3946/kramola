# Если хотите сразу подмешивать это в метаданные:
def inject_surname_exception_meta(item: dict) -> dict:
    surname = item["surname"].strip().upper()
    meta = dict(item.get("meta", {}))

    if surname in MANUAL_REVIEW_Y_DECLENSION:
        # Безопасный компромисс для поиска в тексте:
        # индексируем обе модели, а не только adjective/auto.
        meta.setdefault("y_declension", "both")

    # Для AUTO_INDECLINABLE_IA и AUTO_INDECLINABLE_A_AFTER_VOWEL
    # отдельная мета не нужна: ваш decline_surname_forms уже вернёт constant_forms.
    # Списки оставлены как audit/debug-выгрузка из файла.

    # Составные фамилии лучше решать адресно:
    # meta["hyphen_parts"] = [{}, {"override_forms": constant_forms("Оглы")}]
    item = dict(item)
    item["meta"] = meta
    return item
